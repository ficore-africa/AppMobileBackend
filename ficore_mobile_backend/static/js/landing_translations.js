/**
 * Get current language from localStorage or default to English
 */
function getCurrentLanguage() {
    return localStorage.getItem('landing_language') || 'en';
}

/**
 * Set language preference in localStorage
 */
function setCurrentLanguage(lang) {
    localStorage.setItem('landing_language', lang);
}

/**
 * Get translation for a key in the current language
 */
function t(key, defaultValue = '') {
    const lang = getCurrentLanguage();
    const translations = LANDING_TRANSLATIONS[lang] || LANDING_TRANSLATIONS.en;
    return translations[key] || defaultValue || key;
}

/**
 * Switch language instantly (client-side only)
 */
function switchLanguage(lang) {
    // Validate language
    if (!['en', 'ha', 'pcm'].includes(lang)) {
        console.error('Invalid language:', lang);
        return;
    }
    
    // Save preference
    setCurrentLanguage(lang);
    
    // Update all elements with data-i18n attribute
    document.querySelectorAll('[data-i18n]').forEach(element => {
        const key = element.getAttribute('data-i18n');
        const translation = t(key);
        
        if (element.tagName === 'INPUT' || element.tagName === 'TEXTAREA') {
            element.placeholder = translation;
        } else {
            element.textContent = translation;
        }
    });
    
    // Update page title
    document.title = t('landing_page_title');
    
    // Update language button states
    document.querySelectorAll('.lang-btn').forEach(btn => {
        btn.classList.remove('active');
        const btnLang = btn.getAttribute('data-lang');
        if (btnLang === lang) {
            btn.classList.add('active');
        }
    });
    
    // Update mobile menu language items
    document.querySelectorAll('.mobile-nav-item[data-lang]').forEach(item => {
        const itemLang = item.getAttribute('data-lang');
        const checkmark = item.querySelector('.bi-check-circle-fill');
        
        if (itemLang === lang) {
            item.classList.add('active');
            if (checkmark) checkmark.classList.remove('d-none');
        } else {
            item.classList.remove('active');
            if (checkmark) checkmark.classList.add('d-none');
        }
    });
    
    console.log('Language switched to:', lang);
}

/**
 * Initialize language on page load
 */
function initLanguage() {
    const lang = getCurrentLanguage();
    
    // Apply translations to all elements
    document.querySelectorAll('[data-i18n]').forEach(element => {
        const key = element.getAttribute('data-i18n');
        const translation = t(key);
        
        if (element.tagName === 'INPUT' || element.tagName === 'TEXTAREA') {
            element.placeholder = translation;
        } else {
            element.textContent = translation;
        }
    });
    
    // Update page title
    document.title = t('landing_page_title');
    
    // Set active language button
    document.querySelectorAll('.lang-btn').forEach(btn => {
        const btnLang = btn.getAttribute('data-lang');
        if (btnLang === lang) {
            btn.classList.add('active');
        }
    });
    
    console.log('Language initialized:', lang);
}

// Initialize language when DOM is ready
if (document.readyState === 'loading') {
    document.addEventListener('DOMContentLoaded', initLanguage);
} else {
    initLanguage();
}
